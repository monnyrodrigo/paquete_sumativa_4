#' Suma dos números
#'
#' Esta función suma dos valores numéricos simples.
#'
#' @param x Primer número.
#' @param y Segundo número.
#' @return La suma de x + y.
#' @examples
#' suma.dos.numeros(2, 5)
#' @export
suma.dos.numeros <- function(x, y) {
  x + y
}
